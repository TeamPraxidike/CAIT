<div class="bg-surface-50 rounded-lg w-[50%] p-8">
	<h2 class="font-bold text-lg text-surface-800">How to Create a Circuit</h2>
	<br>
	<h3 class="font-semibold text-md text-surface-800">Add a Publication</h3>
	<p class="text-md pl-4">1. Click on the Insert Publication button</p>
	<p class="text-md pl-4">2. Select the publications you want to add from the menu</p>
	<br>
	<h3 class="font-semibold text-md text-surface-800">Connect Publications</h3>
	<p class="text-md pl-4">To connect two publications</p>
	<p class="text-md pl-8">1. Hover over the source node</p>
	<p class="text-md pl-8">2. Drag the black dot that will appear underneath all the way to the destination node</p>
	<p class="text-md pl-8">3. Release</p>
	<p class="text-md pl-4">To connect multiple publications to one:</p>
	<p class="text-md pl-8">1. Click on the Connect Publications button:</p>
	<p class="text-md pl-8">2. If you haven't selected a node click on one to select it as a source node</p>
	<p class="text-md pl-8">3. All the nodes connected by an incoming edge from the source will turn light grey:</p>
	<p class="text-md pl-8">4. Click on a great node to remove the edge or click on a white node to add an edge:</p>
	<p class="text-md pl-8">5. Click on Save Changes button to save the changes or anywhere outside the nodes:</p>
	<br>
	<h3 class="font-semibold text-md text-surface-800">Remove a Node or an Edge</h3>
	<p class="text-md pl-4">1. Select it (Click on it)</p>
	<p class="text-md pl-4">2. Hold CTRL or CMD to select multiple nodes or edges</p>
	<p class="text-md pl-4">3. Click Backspace or Delete from the keyboard or click the Remove from circuit button</p>
</div>